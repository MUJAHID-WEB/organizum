<script>


export default {
  name: 'About',
};

document.addEventListener("DOMContentLoaded", function () {
  const reviewTrack = document.querySelector(".review_track"); // Target the track container
  const reviews = document.querySelectorAll(".review_part"); // Get all review parts
  const totalReviews = reviews.length;
  let currentIndex = 0;

  const btnLeft = document.querySelector(".btn_left");
  const btnRight = document.querySelector(".btn_right");

  // Disable left button initially
  btnLeft.disabled = true;

  // Set the gap between reviews
  const gap = 32; // 32px gap

  function updateSliderPosition() {
    const reviewWidth = reviews[0].offsetWidth; // Width of one review
    const offset = (reviewWidth + gap) * currentIndex; // Calculate offset with gap
    reviewTrack.style.transform = `translateX(-${offset}px)`;
    updateButtons();
  }

  function nextSlide() {
    if (currentIndex < totalReviews - 1) {
      currentIndex++;
      updateSliderPosition();
    }
  }

  function prevSlide() {
    if (currentIndex > 0) {
      currentIndex--;
      updateSliderPosition();
    }
  }

  function updateButtons() {
    // Disable/enable buttons based on currentIndex
    btnLeft.disabled = currentIndex === 0;
    btnRight.disabled = currentIndex === totalReviews - 1;
  }

  // Event listeners for navigation buttons
  btnLeft.addEventListener("click", function () {
    prevSlide();
  });

  btnRight.addEventListener("click", function () {
    nextSlide();
  });

  // Update slider position on window resize
  window.addEventListener("resize", function () {
    updateSliderPosition();
  });

  // Initialize
  updateButtons();
});
</script>

